# Boolean Test
print(not(1 != 3)) # False
print((3 > 0) and (3 < 5)) # True
print((3 > 0) & (3 < 5)) # True

print((3 > 0) and (3 > 5)) # False > Script 에서는 && 연산자와 동일한 것이다
print((3 > 0) & (3 > 5)) # False > Script 에서는 && 연산자와 동일한 것이다

print((3 > 0) or (3 < 5)) # True
print((3 > 0) | (3 < 5)) # True



